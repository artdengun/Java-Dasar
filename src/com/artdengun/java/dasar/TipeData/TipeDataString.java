package com.artdengun.java.dasar.TipeData;

public class TipeDataString {
    public static void main(String[] args) {

        // String biasa
        String aku ="Seorang programmer java";
        String panggil = "yang sedang belajar dengan semangat";

        // penggabungan String
        String saya = aku + " Dan " + panggil;

        // hasil string biasa
        System.out.println(aku);
        System.out.println(panggil);

        // hasil pengabungan String
        System.out.println(saya);
    }
}
